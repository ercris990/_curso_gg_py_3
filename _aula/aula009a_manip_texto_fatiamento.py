#Cadeia de Caracteres   #fatiamento de string
frase = 'Curso em Video Python'
print(frase)
print(frase[12])            #apresenta a o caracter numero 9 (incluindo os espacos)
print(frase[9:13])          #seleciona os caracteres do 9 ate ao 12 (incluindo os espacos)
print(frase[9:21])          #seleciona os caracteres do 9 ate ao 20 (incluindo os espacos)
print(frase[9:21:2])        #seleciona os caracteres do 9 ate ao 20 (pulando de 2 em 2)
print(frase[:5])            #seleciona os caracteres do inicio ate ao 5
print(frase[15:])           #seleciona os caracteres apartir do 15 ate ao final
print(frase[9::3])          #seleciona os caracteres apartir do 9 ate ao fina (pulando de 3 em 3)

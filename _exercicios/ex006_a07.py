# exercicio o dobro, o triplo e a raiz quadrada
n = int(input('Digite um numero: '))
# --------------------------------------
print('----------------------------')
print('O número digitado foi: ', n)
print('O seu dobro é: {}'.format(n*2))
print('O seu triplo é: {}'.format(n*3))
print('A sua raiz quadrada é: {:.2f}'.format(n**(1/2)))
print('A sua raiz cúbica é: {:.2f}'.format(n**(1/3)))
